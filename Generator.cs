	private void Regenerate(){
		
		float width = dirtPrefab.transform.lossyScale.x;
		float height = dirtPrefab.transform.lossyScale.y;
		
		for (int i = 0; i < 64; i++){//columns (x values
			for(int j = 0; j < 32; j++){//rows (y values)
				if (gameMap [j,i] != 0) {
					GameObject block = selectTileType (gameMap [j,i]);
					Instantiate (block, new Vector2 (i * width, 32-j * height), Quaternion.identity);
				}
			}			
		}
	}

	private GameObject selectTileType(int tileIndex) {
		switch (tileIndex) {
		case 1:
			return grassPrefab;
		case 2:
			return dirtPrefab;
		case 3:
			return waterPrefab;
		case 4: 
			return goldPrefab;
		case 5:
			return cloudPrefab;
		default: 
			return dirtPrefab;
		}
	}

